package com.relief.domain.satellite;

/**
 * Types of damage that can be detected from satellite imagery
 */
public enum DamageType {
    BUILDING_COLLAPSE,     // Building structural damage/collapse
    FLOODING,              // Flood damage
    FIRE,                  // Fire damage
    LANDSLIDE,             // Landslide damage
    DEBRIS,                // Debris accumulation
    INFRASTRUCTURE,        // Infrastructure damage (roads, bridges, etc.)
    VEGETATION,            // Vegetation damage
    EROSION,               // Soil erosion
    CONTAMINATION,         // Environmental contamination
    GENERAL                // General damage (unspecified)
}



