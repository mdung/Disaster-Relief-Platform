package com.relief.domain.indoor;

/**
 * Difficulty levels for indoor navigation routes
 */
public enum DifficultyLevel {
    EASY,        // Easy navigation (wide corridors, no stairs)
    MODERATE,    // Moderate navigation (some stairs, narrow passages)
    DIFFICULT,   // Difficult navigation (many stairs, complex layout)
    EXPERT       // Expert navigation (restricted areas, complex routing)
}



