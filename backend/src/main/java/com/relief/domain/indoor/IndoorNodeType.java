package com.relief.domain.indoor;

/**
 * Types of indoor navigation nodes
 */
public enum IndoorNodeType {
    ROOM,                  // Regular room or space
    CORRIDOR,              // Corridor or hallway
    STAIRS,                // Staircase
    ELEVATOR,              // Elevator
    ENTRANCE,              // Building entrance
    EXIT,                  // Building exit
    EMERGENCY_EXIT,        // Emergency exit
    RESTROOM,              // Restroom facilities
    ELEVATOR_SHAFT,        // Elevator shaft
    MECHANICAL_ROOM,       // Mechanical equipment room
    STORAGE_ROOM,          // Storage area
    OFFICE,                // Office space
    MEETING_ROOM,          // Meeting or conference room
    CAFETERIA,             // Cafeteria or dining area
    LOBBY,                 // Building lobby
    PARKING,               // Parking area
    LOADING_DOCK,          // Loading dock
    CUSTOM                 // Custom node type
}



